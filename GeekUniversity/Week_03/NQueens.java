class Solution {
    //æˆå‘˜å˜é‡ï¼Œå‡å°‘å‚æ•°ä¼ é€’
    private Set<Integer> cols;
    private Set<Integer> pie;
    private Set<Integer> na;
    private int n;
    private List<List<String>> res;

    public List<List<String>> solveNQueens(int n) {
        this.n = n;
        res = new ArrayList<>();
        if (n < 1) return res;
        //ä¹‹å‰çš„çš‡åæ‰€æ”»å‡»çš„ä½ç½®ï¼ˆåˆ—ã€pieã€naï¼‰
        cols = new HashSet<>();
        pie = new HashSet<>();
        na = new HashSet<>();

        Deque<Integer> path = new ArrayDeque<>();
        dfs(0, path);
        return res;
    }

    private void dfs(int row, Deque<Integer> path) {
        if (row == n) {
            List<String> board = convert2board(path);
            res.add(board);
            return;
        }

        //éå†åˆ—columnsï¼Œå°†çš‡åæŒ¨ä¸ªå°è¯•æ”¾ç½®
        for (int i = 0; i < n; i++) {
            //å¦‚æœå½“å‰ä½ç½®ä¸ä¼šå—åˆ°æ”»å‡»ï¼Œåˆ™å¯ä»¥æ”¾ç½®
            if (!cols.contains(i) && !pie.contains(row + i) && !na.contains(row - i)) {
                path.addLast(i);
                cols.add(i);
                pie.add(row + i);
                na.add(row - i);

                //drill down
                dfs(row + 1, path);

                //revert states
                cols.remove(i);
                na.remove(row - i);
                pie.remove(row + i);
                path.removeLast();
            }
        }
    }

    private List<String> convert2board(Deque<Integer> path) {
        List<String> board = new ArrayList<>();
        for (Integer num : path) {
            StringBuilder row = new StringBuilder();
            row.append(".".repeat(Math.max(0, n)));
            row.replace(num, num + 1, "Q");
            board.add(row.toString());
        }
        return board;
    }


}
